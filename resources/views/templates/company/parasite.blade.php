<h4>Las Plagas</h4>
<p style="text-align:justify">
    The Plaga is the only existing parasite able to cause mutagenic reactions upon the host, allowing the host to mutate horrifically. As the parasite is based in the chest and controls all, the removal of the host's head can do nothing but make things worse. In areas of low UV radiation-night time for instance-Las Plagas would be able to use parts of its body as a weapon. <br><br>

    If needed, some subspecies are able to escape and live for a short period of time without a host. Type 2 and Type 3 Plagas were genetically created through experiments by Tricell, include improvements over the original such as making parasite maturation occur in a matter of seconds, and improving strength and agility in the infected, respectively.<br><br>

    It is unclear what is the relation, if any, this parasite has to the NE-α Type, although both types of host show similar symptoms of infestation. </p><br> <br>

